package com.mega.mvcProjectMedia;

//데이터 전달용 가방역할 클래스
public class SportVideoVO {
	private int video_num;
	private String video_code;
	private String video_img;
	private String video_name;
	private String video_category;
	private String video_indate;
	private String video_good;
	private String video_views;
	
	public int getVideo_num() {
		return video_num;
	}
	public void setVideo_num(int video_num) {
		this.video_num = video_num;
	}
	public String getVideo_code() {
		return video_code;
	}
	public void setVideo_code(String video_code) {
		this.video_code = video_code;
	}
	public String getVideo_img() {
		return video_img;
	}
	public void setVideo_img(String video_img) {
		this.video_img = video_img;
	}
	public String getVideo_name() {
		return video_name;
	}
	public void setVideo_name(String video_name) {
		this.video_name = video_name;
	}
	public String getVideo_category() {
		return video_category;
	}
	public void setVideo_category(String video_category) {
		this.video_category = video_category;
	}
	public String getVideo_indate() {
		return video_indate;
	}
	public void setVideo_indate(String video_indate) {
		this.video_indate = video_indate;
	}
	public String getVideo_good() {
		return video_good;
	}
	public void setVideo_good(String video_good) {
		this.video_good = video_good;
	}
	public String getVideo_views() {
		return video_views;
	}
	public void setVideo_views(String video_views) {
		this.video_views = video_views;
	}

	

}
