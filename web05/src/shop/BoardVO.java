package shop;

public class BoardVO {
	String id;
	String title;
	String content;
	String writer;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle() {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent() {
		this.content = content;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter() {
		this.writer = writer;
	}
}
